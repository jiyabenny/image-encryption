% --- Executes on button press in radiobutton5.
function radiobutton5_Callback(hObject, eventdata, handles)
% hObject    handle to radiobutton5 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Hint: get(hObject,'Value') returns toggle state of radiobutton5

%% Three Sequences Generation - Liu System

xo=handles.xo;
yo=handles.yo;
zo=handles.zo;
c=handles.c;
no=handles.no;

h=0.008911;   %% It is given in base paper

%% Chotic State

a=10; 
b=40;
d=4;

t1=yo-xo;
XX=(xo+((h.*a)*t1))/100;
t2=b-zo;
YY=(yo+((h.*xo)*t2))/100;
t3=(c.*zo)+(d.*xo^2);
ZZ=(zo+(h*t3))/100;

X1=randi([0 255],512,512);        %%  512*512 size used based on plain image size

% X1 = 255 + (255+255)*rand(512,512);

X=XX+X1;
axes(handles.axes1);
imshow(uint8(X));
title('Sequence X');

Y=YY+X1;
axes(handles.axes2);
imshow(uint8(Y));
title('Sequence Y');

Z=ZZ+X1;
axes(handles.axes3);
imshow(uint8(X));
title('Sequence Z');

save('initialvalues.mat','xo','yo','zo','c','no','X1');

handles.X=X;
handles.Y=Y;
handles.Z=Z;

% Update handles structure
guidata(hObject, handles);


